  async postAddProduct(req, res) {
    let { pName, pDescription, pPrice, pQuantity, pCategory, pVendor, mfdDate, expDate, pOffer, pStatus } = req.body;
    let images = req.files;
    // Validation
    try {
      // Validate vendor
      // const vendor = "678a1cda35a10525e4780e47";
      // const id = JSON.stringify(vendor);
      // console.log(id);
      // console.log(String(productVendor));
      // const v = String(pVendor);
      // console.log(typeof(v));
      // console.log(ObjectId(v))
      const activeVendor = await vendorModel.findById(pVendor);
      console.log(activeVendor);
      if (!activeVendor) {
        return res.status(400).json({ error: "Vendor is not active or invalid" });
      }
  
      // Validate fields
      if (
        !pName || 
        !pDescription || 
        !pPrice || 
        !pQuantity || 
        !pCategory || 
        !pVendor || 
        !mfdDate ||
        !expDate ||
        !pOffer || 
        !pStatus
      ) {
        Product.deleteImages(images, "file");
        return res.status(400).json({ error: "All fields must be provided" });
      }
  
      // Validate Name and Description Length
      if (pName.length > 255 || pDescription.length > 3000) {
        Product.deleteImages(images, "file");
        return res.status(400).json({
          error: "Name must not exceed 255 characters, and Description must not exceed 3000 characters",
        });
      }
  
      // Validate Images
      if (!images || images.length !== 2) {
        Product.deleteImages(images, "file");
        return res.status(400).json({ error: "Exactly 2 images must be provided" });
      }
  
      // Prepare images
      const allImages = images.map(img => img.filename);
  
      // Save product
      const newProduct = new productModel({
        pImages: allImages,
        pName,
        pDescription,
        pPrice,
        pQuantity,
        pCategory,
        pVendor,
        mfdDate,
        expDate,
        pOffer,
        pStatus,
      });
  
      const savedProduct = await newProduct.save();
      return res.status(201).json( {success: "Product  Created Sucessfully..!"} );
    } catch (err) {
        console.log(err);
      }
  }

